void print_online_status(u8 flag)
{
	u8 i;
	Lcd_Set_Y_Addr(4);
	Lcd_Set_X_Addr(108);
	if(flag == TRUE)
	{
		for(i = 0; i < 24; i++)
			Lcd_Write_Byte(signal_symbol[0][i]);
	}
	else
	{
		for(i = 0; i < 24; i++)
			Lcd_Write_Byte(0x00);
	}
	
	Lcd_Set_Y_Addr(5);
	Lcd_Set_X_Addr(108);
	if(flag == TRUE)
	{
		for(i = 0; i < 24; i++)
			Lcd_Write_Byte(signal_symbol[1][i]);
	}
	else
	{
		for(i = 0; i < 24; i++)
			Lcd_Write_Byte(0x00);
	}
}

